    public void UpdateCreateTableData() {
        try {

            ResultSet rs = DataBaseHandler.excecuteQuery("SELECT * FROM app.BOOKINGS WHERE ROOMNUMBER = " + Integer.parseInt((RoomsComboBox.getSelectedItem().toString()).substring(5)) + "AND BOOKING_DATE = '" + Datefield.getText() + "'");

            String[] columns = new String[rs.getMetaData().getColumnCount() - 3];

            columns[0] = rs.getMetaData().getColumnLabel(2);
            columns[1] = rs.getMetaData().getColumnLabel(3);
            columns[2] = rs.getMetaData().getColumnLabel(4);

            rs.last();
            int TotalRows = rs.getRow();

            rs.first();
            Object[][] rows = new Object[TotalRows][columns.length];

            for (int i = 0; i < TotalRows; i++) {
                for (int j = 0; j < columns.length; j++) {
                    rows[i][j] = rs.getObject(columns[j]);
                }
                rs.next();
            }

            AddBookingTable.setModel(new javax.swing.table.DefaultTableModel(rows, columns));

        } catch (Exception e) {
            System.out.println("Error: " + e);
        }
    }

