    private void RoomsComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_RoomsComboBoxItemStateChanged
        UpdateCreateTableData();
        String Room = RoomsComboBox.getSelectedItem().toString();
        int Capacity = 0;
        String WheelchairAccess = "No Wheelchair Access";
        WheelChairAccessText.setForeground(new Color(255, 50, 50));

        switch (Integer.parseInt(Room.substring(5))) {
            case 1:
                Capacity = 2;
                break;
            case 2:
                Capacity = 4;
                break;
            case 3:
                Capacity = 8;
                break;
            case 4:
                Capacity = 15;
                WheelchairAccess = "Has Wheelchair Access";
                WheelChairAccessText.setForeground(Color.green);
                break;
            case 5:
                Capacity = 50;
                break;
        }

        RoomCapacityText.setText("Capacity: " + Capacity);
        WheelChairAccessText.setText(WheelchairAccess);

    }//GEN-LAST:event_RoomsComboBoxItemStateChanged

